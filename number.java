// repeating number using for loop
import java.util.Scanner;
public class number{
  public static void main(String args[]){
    Scanner sc=new Scanner(System.in);
    System.out.println("enter a number");
    int n=sc.nextInt();
    for(int i=1;i<n;i++){
      System.out.println(n);
    }
      
  }
  
}
